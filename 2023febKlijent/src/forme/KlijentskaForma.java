    public void updateOnlineKorisnike(ArrayList<Korisnik> online) {
        cb.removeAllItems();
        online.remove(ulogovani);
        onlineKorisnici = online;
        for (Korisnik k: onlineKorisnici){
            cb.addItem(k.getUser());
        }
    }

    public void stiglaPoruka(Poruka poruka) {
        poruke.add(poruka);
        popuniTabelu();
    }

    private void popuniTabelu() {
        model.setRowCount(0);
        for (Poruka p: poruke){
            dodajUTabelu(p);
        }
    }

    private void pripremiTabelu() {
        model.addColumn("Vreme");
        model.addColumn("Od");
        
        model.addColumn("Tekst");
        model.addColumn("Kome");
        
        tabela.setModel(model);
    }

    private void dodajUTabelu(Poruka p) {
        Object[] o = {vr.format(p.getDatum()), p.getOD().getUser(), p.getTekst(), p.getDO() == null ? "ALL" : "meni"};
        model.addRow(o);
    }
