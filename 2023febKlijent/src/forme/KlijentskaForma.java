    private void btnSamoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSamoActionPerformed
        if (onlineKorisnici.isEmpty()) return;
        if (txtPoruka.getText().isEmpty()) return;
        Korisnik KOME = onlineKorisnici.get(cb.getSelectedIndex());
        Poruka por = new Poruka(ulogovani, KOME, txtPoruka.getText(), new Date());
        Komunikacija.getInstance().posaljiPoruku(por);
        
        txtPoruka.setText("");
    }//GEN-LAST:event_btnSamoActionPerformed

