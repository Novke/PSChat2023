    private void btnprikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprikaziActionPerformed
        int izbor = tabela.getSelectedRow();
        if (izbor==-1) return;
        Poruka poruka = poruke.get(izbor);
        if (poruka.getTekst().length()<20) JOptionPane.showMessageDialog(rootPane, "Mora poruka imati bar 20 karaktera");
        else {
            new PrikazPoruke(this, true, poruka).setVisible(true);
        }
        
    }//GEN-LAST:event_btnprikaziActionPerformed


